SELECT MEMBER_ID, CONTACT_ID FROM MEMBER JOIN CONTACT USING(member_id) ;
SELECT M.MEMBER_ID, C.CONTACT_ID FROM MEMBER M, CONTACT C WHERE M.MEMBER_ID = C.MEMBER_ID;
SELECT CONTACT_ID, ADDRESS_ID FROM CONTACT JOIN ADDRESS USING(CONTACT_ID) ;
SELECT C.CONTACT_ID, ADDRESS_ID FROM CONTACT C, ADDRESS A WHERE C.CONTACT_ID = A.CONTACT_ID;
SELECT ADDRESS_ID, STREET_ADDRESS_ID FROM ADDRESS JOIN STREET_ADDRESS USING(address_id) ;
SELECT A.ADDRESS_ID, STREET_ADDRESS_ID FROM ADDRESS A, STREET_ADDRESS S WHERE A.ADDRESS_ID = S.ADDRESS_ID;
SELECT ADDRESS_ID, TELEPHONE_ID FROM ADDRESS JOIN TELEPHONE USING(address_id) ;
SELECT A.ADDRESS_ID, TELEPHONE_ID FROM ADDRESS A, TELEPHONE T WHERE A.ADDRESS_ID = T.ADDRESS_ID;

