COL created_by      FORMAT 999999  HEADING "Created|By|ID #|--------|Table #1"
COL system_user_pk  FORMAT 999999  HEADING "System|User|ID #|--------|Table #2"

SELECT  su1.SYSTEM_USER_ID, su2.CREATED_BY, su3.SYSTEM_USER_ID AS system_user_pk FROM SYSTEM_USER SU1 INNER JOIN SYSTEM_USER SU2 ON su1.LAST_UPDATED_BY = su2.SYSTEM_USER_ID INNER JOIN SYSTEM_USER SU3 ON su1.LAST_UPDATED_BY = su3.SYSTEM_USER_ID;

COL system_user_id   FORMAT 999999  HEADING "System|User|ID #|--------|Table #1"
COL last_updated_by  FORMAT 999999  HEADING "Last|Updated|By|ID #|--------|Table #1"
COL system_user_pk   FORMAT 999999  HEADING "System|User|ID #|--------|Table #2"

SELECT  su1.SYSTEM_USER_ID, su2.LAST_UPDATED_BY, su3.SYSTEM_USER_ID AS system_user_pk FROM SYSTEM_USER SU1 INNER JOIN SYSTEM_USER SU2 ON su1.LAST_UPDATED_BY = su2.SYSTEM_USER_ID INNER JOIN SYSTEM_USER SU3 ON su1.LAST_UPDATED_BY = su3.SYSTEM_USER_ID;


COL user_id        FORMAT 999999  HEADING "System|User|ID #|--------|Table #1"
COL user_name      FORMAT A8      HEADING "System|User|Name|--------|Table #1"
COL cby_user_id    FORMAT 999999  HEADING "System|User|ID #|--------|Table #2"
COL cby_user_name  FORMAT A8      HEADING "System|User|Name|--------|Table #2"
COL lby_user_id    FORMAT 999999  HEADING "System|User|ID #|--------|Table #3"
COL lby_user_name  FORMAT A8      HEADING "System|User|Name|--------|Table #3"

SELECT su1.SYSTEM_USER_ID AS user_id, su1.SYSTEM_USER_NAME AS user_name, su2.SYSTEM_USER_ID AS cby_user_id, su2.SYSTEM_USER_NAME AS cby_user_name, su3.SYSTEM_USER_ID AS lby_user_id, su3.SYSTEM_USER_NAME AS lby_user_name  FROM SYSTEM_USER SU1 INNER JOIN SYSTEM_USER SU2 ON su1.CREATED_BY  = su2.SYSTEM_USER_ID INNER JOIN SYSTEM_USER SU3 ON su1.LAST_UPDATED_BY = su3.SYSTEM_USER_ID;

